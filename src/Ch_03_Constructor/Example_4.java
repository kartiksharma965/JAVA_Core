package Ch_03_Constructor;

public class Example_4 {
	
	Example_4(){
		System.out.println("I am Default constructor from Parent class Example_4");
	}
	
	Example_4(int a){
		System.out.println("I am Integer constructor from Parent class Example_4");
	}

	Example_4(String a){
		System.out.println("I am String constructor from Parent class Example_4");
	}
}
